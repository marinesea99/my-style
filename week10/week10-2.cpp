#include <stdio.h>

int main()
{
    int a=100;
    int b=200;
    int c;
    printf("a:%d b:%d c:%d\n",a,b,c);
    ///一次宣告很多個變數
    int d[3]={100,200,300};
    printf("d[0]:%d\n",d[0]);
    printf("d[1]:%d\n",d[1]);
    printf("d[2]:%d\n",d[2]);

    int f[3];
    printf("f[0]:%d\n",f[0]);
    printf("f[1]:%d\n",f[1]);
    printf("f[2]:%d\n",f[2]);
}
